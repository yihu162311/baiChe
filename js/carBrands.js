var brandBox = document.getElementById("brandBox");
function createBrandBox(option){
	var fragment = document.createDocumentFragment(),
		data = option.forEach(function(item){
			var li = document.createElement("li"),
				initial = document.createElement("div"),
				brands = document.createElement("div"),
				data = item.brands;
			initial.className = "initial";
			initial.innerText = item.initial;
			li.appendChild(initial);
			brands.className = "brands";
			data.forEach(function(item1){
				var a = document.createElement("a");
				a.href = item1.href;
				a.innerText = item1.brand;
				return brands.appendChild(a);
			});
			li.appendChild(brands);
			return fragment.appendChild(li);
		});
		brandBox.appendChild(fragment);
}
