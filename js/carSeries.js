var carSeries = document.getElementById("carSeries");
function createSeriesBox(option){
	var fragment = document.createDocumentFragment(),
	data = option.forEach(function(item){
		var li = document.createElement("li"),
			initial = document.createElement("div"),
			series = document.createElement("div"),
			data = item.carSeries;
		initial.className = "initial";	
		initial.innerText = item.initial;
		li.appendChild(initial);
		series.className = "series";
		data.forEach(function(item1){
			var oneSeries = document.createElement("div"),
				a = document.createElement("a"),
				carNameBox = document.createElement("div"),
				label = item1.seriesLabel,
				name = item1.seriesName;
				
			oneSeries.className = "oneSeries";	
			a.className = "seriesName";
			a.href = label.href;
			a.innerText = label.name;
			oneSeries.appendChild(a);
			name.forEach(function(item2){
					var a = document.createElement("a");
					a.href = item2.href;
					a.innerText = item2.name;
					carNameBox.appendChild(a);
					return oneSeries.appendChild(carNameBox);
			});
			carNameBox.className = "carNameBox";
			return series.appendChild(oneSeries);
		});
		li.appendChild(series);
		return fragment.appendChild(li);
	});
	carSeries.appendChild(fragment);
}
