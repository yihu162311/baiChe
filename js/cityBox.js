var cityBox = document.getElementById("cityBox");
function createCityBox(option){
	var fragment = document.createDocumentFragment(),
		data = option.forEach(function(item){
			var li = document.createElement("li"),
				h2 = document.createElement("h2"),
				cities = item.cities;
			h2.innerText = item.title;
			li.appendChild(h2);
			cities.forEach(function(item1){
				var a = document.createElement("a");
				a.href = item1.href;
				a.innerText = item1.city;
				return li.appendChild(a);
			});
			return fragment.appendChild(li);
		});
	cityBox.appendChild(fragment);
}
